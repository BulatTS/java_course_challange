package ru.javacourse;

public class TaskT01N05 {
    public static double converterFromRublesToEuro(int rubles){
        double euros = rubles * 68.34;
        return euros;
    }
    public static double converterFromRublesToDollars(int rubles){
        double dollars = rubles * 61.61;
        return dollars;
    }
    public static double converterFromRublesToGrivna(int rubles){
        double grivna = rubles * 0.40;
        return grivna;
    }
}
